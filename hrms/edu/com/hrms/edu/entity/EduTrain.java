package com.hrms.edu.entity;

public class EduTrain {
	private String treId;
	private String treName;
	private String treNumber;
	public String getTreId() {
		return treId;
	}
	public void setTreId(String treId) {
		this.treId = treId;
	}
	public String getTreName() {
		return treName;
	}
	public void setTreName(String treName) {
		this.treName = treName;
	}
	public String getTreNumber() {
		return treNumber;
	}
	public void setTreNumber(String treNumber) {
		this.treNumber = treNumber;
	}
}
