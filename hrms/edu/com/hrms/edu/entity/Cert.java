package com.hrms.edu.entity;

public class Cert {
	private String cId;
	private String cName;
	private String cStu;
	private String cTec;
	private String cDate;
	public String getcId() {
		return cId;
	}
	public void setcId(String cId) {
		this.cId = cId;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public String getcStu() {
		return cStu;
	}
	public void setcStu(String cStu) {
		this.cStu = cStu;
	}
	public String getcTec() {
		return cTec;
	}
	public void setcTec(String cTec) {
		this.cTec = cTec;
	}
	public String getcDate() {
		return cDate;
	}
	public void setcDate(String cDate) {
		this.cDate = cDate;
	}
}
