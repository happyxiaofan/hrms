package com.hrms.pa.entity;

public class ResignEmployee {
	private int resignno;
	private String emp_id;
	private String emp_name;
	private String resign_reason;
	private String resign_class;
	private String resign_date;
	public int getResignno() {
		return resignno;
	}
	public void setResignno(int resignno) {
		this.resignno = resignno;
	}
	public String getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getResign_reason() {
		return resign_reason;
	}
	public void setResign_reason(String resign_reason) {
		this.resign_reason = resign_reason;
	}
	public String getResign_class() {
		return resign_class;
	}
	public void setResign_class(String resign_class) {
		this.resign_class = resign_class;
	}
	public String getResign_date() {
		return resign_date;
	}
	public void setResign_date(String resign_date) {
		this.resign_date = resign_date;
	}
	
}
